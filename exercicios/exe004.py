n =(input('Digite algo '))
print('Esse dado é de qual tipo primitivo: ', type(n))
print('Esse dado é númerico? ', n.isnumeric())
print('Esse dado é alpha numérico? ', n.isalnum())
print('Esse dado foi digitado todo em letra minúscula? ', n.islower())
print('Esse dado foi digitado todo em letra maiúscula? ', n.isupper())
print('Esse dado foi digitado de forma capitalizada? ', n.istitle())
print('Esse dado é alfabético? ', n.isalpha())
print('Esse dado só tem espaços?', n.isspace())
